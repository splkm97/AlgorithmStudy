#include <bits/stdc++.h>

using namespace std;

int main() {
	ios::sync_with_stdio(0);
	cin.tie(0);

	int tests;
	cin >> tests;

	for (int tc = 1; tc <= tests; tc++) {
		int num; cin >> num;
		vector<int> vec;
		for (int i = 0; i < num; i++) {
			int temp;
			cin >> temp;
			vec.push_back(temp);
		}
	}

	return 0;
}